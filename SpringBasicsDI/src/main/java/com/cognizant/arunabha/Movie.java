package com.cognizant.arunabha;

import java.util.List;

public class Movie {

    private String name;
    private List<String> genre;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<String> getGenre() {
        return genre;
    }

    public void setGenre(List<String> genre) {
        this.genre = genre;
    }



}
